const PostsPages = {
    changeIndexToNext(number){
        let currentIndex = document.getElementById('PostPageIndex');
        currentIndex.innerText = `Página ${number + 1}`;
    },
    
    changeIndexToAfter(number){
        let currentIndex = document.getElementById('PostPageIndex');
        currentIndex.innerText = `Página ${number - 1}`;
    },

    moveOnToTheNextPage(currentPage){
        let text = currentPage.innerText;

        let numberPage = Number(text.replace(/\D/g, ''));

        document.getElementById(`Page${numberPage}`).classList.remove('NotHidden');
        
        let nextPage = document.getElementById(`Page${numberPage + 1}`);
        nextPage.setAttribute('class', 'PostsPage NotHidden');

        PostsPages.changeIndexToNext(numberPage);
    },

    moveOnToTheAfterPage(currentPage){
        let text = currentPage.innerText;

        let numberPage = Number(text.replace(/\D/g, ''));

        document.getElementById(`Page${numberPage}`).classList.remove('NotHidden');
        
        let nextPage = document.getElementById(`Page${numberPage - 1}`);
        nextPage.setAttribute('class', 'PostsPage NotHidden');

        PostsPages.changeIndexToAfter(numberPage);
    }
}

