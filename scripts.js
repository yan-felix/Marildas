const Storege = {
    get() {
        return JSON.parse(localStorage.getItem("Datas:")) || {};
    },

    set(SubscriberDatas) {
        localStorage.setItem("Datas:", JSON.stringify(SubscriberDatas));
    }
}

const App = {
    inIt(){
        let confereSituation = Storege.get();
        if (confereSituation.situation == "subscribed"){
            let a = document.createElement('a');
            a.setAttribute('class', 'nav-link navlink subscribe-Link');
            a.setAttribute('href', '#');
            a.setAttribute('onclick', 'Subscription.clickToUnFollow()');
            a.innerHTML = `
                <h6>Seguindo</h6>
                <svg xmlns="http://www.w3.org/2000/svg" width="24" height="100%" fill="currentColor" class="bi bi-check" viewBox="0 0 16 16">
                <path d="M10.97 4.97a.75.75 0 0 1 1.07 1.05l-3.99 4.99a.75.75 0 0 1-1.08.02L4.324 8.384a.75.75 0 1 1 1.06-1.06l2.094 2.093 3.473-4.425a.267.267 0 0 1 .02-.022z"/>
                </svg>
            `;

            document.querySelector(".SubscribeLink").appendChild(a);
        }else{
            let a = document.createElement('a');
            a.setAttribute('class', 'nav-link navlink subscribe-Link');
            a.setAttribute('href', '#');
            a.setAttribute('onclick', 'Subscription.clickToFollow()');
            a.innerHTML = `<h6>Seguir</h6>`;

            document.querySelector(".SubscribeLink").appendChild(a);
        }
    },

    reload(){
        let subscribeLink = document.querySelector(".subscribe-Link");
        subscribeLink.parentNode.removeChild(subscribeLink);

        App.inIt();
    },
}

const PostsPages = {
    changeIndexToNext(number){
        let currentIndex = document.getElementById('PostPageIndex');
        currentIndex.innerText = `Página ${number + 1}`;
    },
    
    changeIndexToAfter(number){
        let currentIndex = document.getElementById('PostPageIndex');
        currentIndex.innerText = `Página ${number - 1}`;
    },

    moveOnToTheNextPage(currentPage){
        let text = currentPage.innerText;

        let numberPage = Number(text.replace(/\D/g, ''));

        document.getElementById(`Page${numberPage}`).classList.remove('NotHidden');
        
        let nextPage = document.getElementById(`Page${numberPage + 1}`);
        nextPage.setAttribute('class', 'PostsPage NotHidden');

        PostsPages.changeIndexToNext(numberPage);
    },

    moveOnToTheAfterPage(currentPage){
        let text = currentPage.innerText;

        let numberPage = Number(text.replace(/\D/g, ''));

        document.getElementById(`Page${numberPage}`).classList.remove('NotHidden');
        
        let nextPage = document.getElementById(`Page${numberPage - 1}`);
        nextPage.setAttribute('class', 'PostsPage NotHidden');

        PostsPages.changeIndexToAfter(numberPage);
    }
}

